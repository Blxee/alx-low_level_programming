/**
 * op_add - performs addition on two integers
 *
 * @a: first operand
 * @b: second operand
 *
 * Return: the result of the operation
 */
int op_add(int a, int b)
	{ return (a + b); }

/**
 * op_sub - performs subraction on two integers
 *
 * @a: first operand
 * @b: second operand
 *
 * Return: the result of the operation
 */
int op_sub(int a, int b)
	{ return (a - b); }

/**
 * op_mul - performs multiplication on two integers
 *
 * @a: first operand
 * @b: second operand
 *
 * Return: the result of the operation
 */
int op_mul(int a, int b)
	{ return (a * b); }

/**
 * op_div - performs division on two integers
 *
 * @a: first operand
 * @b: second operand
 *
 * Return: the result of the operation
 */
int op_div(int a, int b)
	{ return (a / b); }

/**
 * op_mod - performs modulu on two integers
 *
 * @a: first operand
 * @b: second operand
 *
 * Return: the result of the operation
 */
int op_mod(int a, int b)
	{ return (a % b); }

